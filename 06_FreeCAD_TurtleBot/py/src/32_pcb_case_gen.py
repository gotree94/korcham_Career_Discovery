# ============================================================

def 조립시각화_생성(하단, 상단, 오프셋z=50):
    """
    상단 커버를 위로 올려서 조립 상태를 시각화한다.

    매개변수:
        하단 (Part.Shape): 하단 케이스
        상단 (Part.Shape): 상단 커버
        오프셋z (float): 상단 커버 z 방향 오프셋 (mm)

    반환값:
        list: [하단형태, 상단형태]
    """
    # 상단 커버를 z 방향으로 이동
    변환행렬 = Base.Matrix()
    변환행렬.move(Base.Vector(0, 0, 오프셋z))
    상단이동 = 상단.copy()
    상단이동 = 상단이동.transformGeometry(변환행렬)

    print(f"[정보] 조립 시각화 생성 (상단 z={오프셋z}mm)")
    return [하단, 상단이동]


