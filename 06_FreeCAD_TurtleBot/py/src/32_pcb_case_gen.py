# 나사 홀 상세 생성
# ============================================================

def 나사홀상세생성(형태, 위치, 방향, 파라미터=None):
    """
    상세 나사 홀을 생성한다. 나사산과 팔아헤드가 있는 홀.

    매개변수:
        형태 (Part.Shape): 형태
        위치 (Base.Vector): 홀 위치
        방향 (Base.Vector): 홀 방향
        파라미터 (케이스파라미터): 케이스 파라미터

    반환값:
        Part.Shape: 나사 홀이 추가된 형태
    """
    if 파라미터 is None:
        파라미터 = 케이스파라미터()

    나사지름 = 3.0          # M3 나사
    팔아헤드지름 = 5.5      # 팔아헤드 지름
    팔아헤드깊이 = 2.0      # 팔아헤드 깊이

    # 나사 홀
    나사홀 = Part.makeCylinder(
        나사지름 / 2, 100,
        Base.Vector(위치.x, 위치.y, 위치.z),
        Base.Vector(방향.x, 방향.y, 방향.z)
    )
    형태 = 형태.cut(나사홀)

    # 팔아헤드 홀 (상단)
    팔아홀 = Part.makeCylinder(
        팔아헤드지름 / 2, 팔아헤드깊이,
        Base.Vector(위치.x, 위치.y, 위치.z),
        Base.Vector(방향.x, 방향.y, 방향.z)
    )
    형태 = 형태.cut(팔아홀)

    return 형태


