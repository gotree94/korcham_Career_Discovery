class AI리뷰어:
    """
    AI를 활용한 전문 설계 리뷰 시스템.

    동작 방식:
        - 오프라인: 규칙 기반 분석 + 점수화
        - 온라인: LLM API를 통한 전문가 수준 리뷰
    """

    def __init__(self):
        """리뷰어 초기화"""
        self.가중치 = {
            "형상": 0.20,
            "치수": 0.20,
            "구조": 0.20,
            "제조": 0.20,
            "기능": 0.20,
        }

    def 오프라인_리뷰(self, 모델특성객체, 규칙결과):
        """
        규칙 기반 오프라인 리뷰를 수행합니다.

        매개변수:
            모델특성객체 (모델특성): 모델 특성
            규칙결과 (list): 규칙 검사 결과

        반환값:
            dict: 리뷰 결과
        """
        print("\n[AI 리뷰어] 오프라인 규칙 기반 리뷰 수행...")

        # 카테고리별 통과율 계산
        카테고리별 = {}
        for 결과 in 규칙결과:
            카테고리 = 결과["카테고리"]
            if 카테고리 not in 카테고리별:
                카테고리별[카테고리] = {"통과": 0, "전체": 0}
            카테고리별[카테고리]["전체"] += 1
            if 결과["통과"]:
                카테고리별[카테고리]["통과"] += 1

        # 카테고리별 점수 계산 (0~100)
        카테고리점수 = {}
        for 카테고리, 집계 in 카테고리별.items():
            if 집계["전체"] > 0:
                카테고리점수[카테고리] = (집계["통과"] / 집계["전체"]) * 100
            else:
                카테고리점수[카테고리] = 50.0  # 데이터 없음

        # 종합 점수 계산
        종합점수 = 0.0
        for 카테고리, 가중치 in self.가중치.items():
            점수 = 카테고리점수.get(카테고리, 50.0)
            종합점수 += 점수 * 가중치

        # 등급 결정
        if 종합점수 >= 90:
            등급 = "A"
            등급설명 = "우수 - 뛰어난 설계"
        elif 종합점수 >= 75:
            등급 = "B"
            등급설명 = "양호 - 개선 여지 있으나 양호"
        elif 종합점수 >= 60:
            등급 = "C"
            등급설명 = "보통 - 주요 개선 필요"
        elif 종합점수 >= 40:
            등급 = "D"
            등급설명 = "미흡 - 상당한 개선 필요"
        else:
            등급 = "F"
            등급설명 = "불합격 - 재설계 권장"

        # 개선 제안 생성
        개선제안 = []
        for 결과 in 규칙결과:
            if not 결과["통과"]:
                if 결과["심각도"] == "오류":
                    개선제안.append(f"[반드시] {결과['이름']}: {결과['설명']}")
                elif 결과["심각도"] == "경고":
                    개선제안.append(f"[권장] {결과['이름']}: {결과['설명']} 개선 검토")
                else:
                    개선제안.append(f"[참고] {결과['이름']}: {결과['설명']} 검토")

        # 추가 분석 제안
        if 모델특성객체.복잡도점수 > 6:
            개선제안.append("[권장] 형상 복잡도가 높습니다. 단순화 검토")
