class 모델스냅샷:
    """
    특정 시점의 모델 상태를 저장하는 스냅샷 클래스.

    저장 정보:
        - 버전 번호
        - 타임스탬프
        - 치수 데이터
        - 속성 데이터
        - 설명/커밋 메시지
        - 해시값 (무결성 검증)
    """

    def __init__(self, 버전, 설명=""):
        self.버전 = 버전
        self.타임스탬프 = datetime.datetime.now().isoformat()
        self.설명 = 설명
        self.치수 = {}            # {이름: 값} 형태의 치수 딕셔너리
        self.속성 = {}            # 자유 속성
        self.형태해시 = ""        # 형상 무결성용 해시
        self.태그 = []            # 태그 목록 (예: "최종", "프로토타입")

    def to_dict(self):
        """딕셔너리로 변환"""
        return {
            "버전": self.버전,
            "타임스탬프": self.타임스탬프,
            "설명": self.설명,
            "치수": self.치수,
            "속성": self.속성,
            "형태해시": self.형태해시,
            "태그": self.태그,
        }

    @classmethod
    def from_dict(cls, 데이터):
        """딕셔너리에서 복원"""
        스냅샷 = cls(데이터["버전"], 데이터.get("설명", ""))
        스냅샷.타임스탬프 = 데이터.get("타임스탬프", "")
        스냅샷.치수 = 데이터.get("치수", {})
        스냅샷.속성 = 데이터.get("속성", {})
        스냅샷.형태해시 = 데이터.get("형태해시", "")
        스냅샷.태그 = 데이터.get("태그", [])
        return 스냅샷


