def 판재_두께_계산(조건):
    """
    단순 지지 판재의 최소 두께를 계산합니다.

    공식: sigma = F / (b * t)
    => t = F * 안전율 / (b * 허용응력)

    매개변수:
        조건 (설계조건): 설계 조건

    반환값:
        dict: 계산 결과
    """
    # 재료 특성 가져오기
    재료 = 재료_데이터베이스.get(조건.재료명)
    if not 재료:
        print(f"[오류] '{조건.재료명}' 재료를 찾을 수 없습니다.")
        return None

    허용응력 = 재료["타선응력_MPa"]  # 항복 응력 사용
    안전응력 = 허용응력 / 조건.안전율  # 허용 응력 (안전율 적용)

    # 판재 폭을 길이의 1/10으로 가정 (지지 조건에 따라 다름)
    폭_mm = 조건.길이_mm / 10.0

    # 최소 두께 계산
    # sigma = F / (b * t) => t = F / (b * sigma)
    최소두께_mm = (조건.하중_N / (폭_mm * 안전응력))

    # 반올림 (실제 제작 고려, 0.5mm 단위)
    권장두께_mm = math.ceil(최소두께_mm * 2) / 2.0

    # 최소 두께 보장 (구조적 최소 두께)
    if 권장두께_mm < 1.0:
        권장두께_mm = 1.0

    결과 = {
        "계산유형": "판재 두께",
        "재료": 조건.재료명,
        "허용응력_MPa": 허용응력,
        "안전응력_MPa": 안전응력,
        "하중_N": 조건.하중_N,
        "폭_mm": 폭_mm,
        "최소두께_mm": 최소두께_mm,
        "권장두께_mm": 권장두께_mm,
        "검증_응력_MPa": 조건.하중_N / (폭_mm * 권장두께_mm)
    }

    return 결과


def 원통_지름_계산(조건):
    """
    축이나 실린더의 최소 지름을 계산합니다.

    공식: sigma = F / (pi * r^2)
    => r = sqrt(F * 안전율 / (pi * 허용응력))

    매개변수:
        조건 (설계조건): 설계 조건

    반환값:
        dict: 계산 결과
    """
    재료 = 재료_데이터베이스.get(조건.재료명)
    if not 재료:
        return None

    허용응력 = 재료["타선응력_MPa"]
    안전응력 = 허용응력 / 조건.안전율

    # 인장 하중 기준 최소 지름
    # sigma = F / A = F / (pi * r^2)
    # r = sqrt(F / (pi * sigma))
    최소반지름 = math.sqrt(조건.하중_N / (math.pi * 안전응력))
    최소지름 = 최소반지름 * 2

    # 반올림 (1mm 단위)
    권장지름 = math.ceil(최소지름)

    # 최소 지름 보장
    if 권장지름 < 5.0:
        권장지름 = 5.0

    # 실제 응력 검증
    면적 = math.pi * (권장지름 / 2) ** 2
    실제응력 = 조건.하중_N / 면적

    결과 = {
        "계산유형": "원통 지름",
        "재료": 조건.재료명,
        "허용응력_MPa": 허용응력,
        "안전응력_MPa": 안전응력,
        "하중_N": 조건.하중_N,
        "최소지름_mm": 최소지름,
        "권장지름_mm": 권장지름,
        "실제응력_MPa": 실제응력,
        "안전율_검증": 허용응력 / 실제응력
    }

    return 결과


def 빔_단면_계산(조건):
    """
    단순 지지 빔의 최소 단면 계수를 계산합니다.

    공식: M = F * L / 4 (중앙 하중)
          sigma = M / S
          S = b * h^2 / 6 (직사각형 단면)

    매개변수:
        조건 (설계조건): 설계 조건

    반환값:
        dict: 계산 결과
    """
    재료 = 재료_데이터베이스.get(조건.재료명)
    if not 재료:
        return None

    허용응력 = 재료["타선응력_MPa"]
    안전응력 = 허용응력 / 조건.안전율

    # 최대 굽힘 모멘트 (중앙 하중, 단순 지지)
    모멘트_Nmm = 조건.하중_N * 조건.길이_mm / 4.0

    # 단면계수: S = M / sigma
    최소단면계수 = 모멘트_Nmm / 안전응력

    # 폭/높이 비율 1:2 가정 (h = 2b)
    # S = b * h^2 / 6 = b * (2b)^2 / 6 = 4b^3 / 6 = 2b^3 / 3
    # b = (3S/2)^(1/3)
    최소폭 = (3 * 최소단면계수 / 2) ** (1.0 / 3.0)
    최소높이 = 최소폭 * 2

    # 반올림
    권장폭 = math.ceil(최소폭)
    권장높이 = math.ceil(최소높이)

    # 검증
    검증단면계수 = 권장폭 * 권장높이 ** 2 / 6.0
    검증응력 = 모멘트_Nmm / 검증단면계수

    결과 = {
        "계산유형": "빔 단면",
        "재료": 조건.재료명,
        "허용응력_MPa": 허용응력,
        "모멘트_Nmm": 모멘트_Nmm,
        "최소단면계수_mm3": 최소단면계수,
        "권장폭_mm": 권장폭,
        "권장높이_mm": 권장높이,
        "검증응력_MPa": 검증응력,
        "안전율_검증": 허용응력 / 검증응력
    }

    return 결과


