def 추천_검증(조건, 계산결과):
    """
    공학적 계산 결과를 검증하고 리포트를 생성합니다.

    검증 항목:
    1. 응력 검증: 실제 응력 < 허용 응력
    2. 안전율 검증: 실제 안전율 >= 요구 안전율
    3. 온도 검증: 사용 온도 < 최대 사용 온도
    4. 변형 검증: 허용 변형량 이내

    매개변수:
        조건 (설계조건): 설계 조건
        계산결과 (dict): 계산 결과

    반환값:
        dict: 검증 결과
    """
    if not 계산결과:
        return {"통과": False, "메시지": "계산 결과가 없습니다."}

    재료 = 재료_데이터베이스.get(조건.재료명, {})
    검증_결과 = []

    # 1. 응력 검증
    허용응력 = 재료.get("타선응력_MPa", 0)
    if "검증_응력_MPa" in 계산결과:
        실제응력 = 계산결과["검증_응력_MPa"]
    elif "실제응력_MPa" in 계산결과:
        실제응력 = 계산결과["실제응력_MPa"]
    else:
        실제응력 = 0

    if 허용응력 > 0 and 실제응력 > 0:
        if 실제응력 < 허용응력:
            검증_결과.append({
                "항목": "응력 검증",
                "통과": True,
                "메시지": f"OK: {실제응력:.1f} MPa < {허용응력} MPa"
            })
        else:
            검증_결과.append({
                "항목": "응력 검증",
                "통과": False,
                "메시지": f"실패: {실제응력:.1f} MPa >= {허용응력} MPa"
            })

    # 2. 안전율 검증
    if "안전율_검증" in 계산결과:
        실제_안전율 = 계산결과["안전율_검증"]
        if 실제_안전율 >= 조건.안전율:
            검증_결과.append({
                "항목": "안전율 검증",
                "통과": True,
                "메시지": f"OK: {실제_안전율:.2f} >= {조건.안전율}"
            })
        else:
            검증_결과.append({
                "항목": "안전율 검증",
                "통과": False,
                "메시지": f"실패: {실제_안전율:.2f} < {조건.안전율}"
            })

    # 3. 온도 검증
    최대온도 = 재료.get("최대사용온도_C", 9999)
    if 조건.온도_C <= 최대온도:
        검증_결과.append({
            "항목": "온도 검증",
            "통과": True,
            "메시지": f"OK: {조건.온도_C}C <= {최대온도}C"
        })
    else:
        검증_결과.append({
            "항목": "온도 검증",
            "통과": False,
            "메시지": f"실패: {조건.온도_C}C > {최대온도}C"
        })

    # 4. 재료 호환성 검증
    내식성 = 재료.get("내식성", "보통")
    환경_적합 = True
    if 조건.환경 == "해양" and 내식성 not in ["우수"]:
        환경_적합 = False
    if 조건.환경 == "화학" and 내식성 not in ["우수"]:
        환경_적합 = False

    if 환경_적합:
        검증_결과.append({
            "항목": "환경 적합성",
            "통과": True,
            "메시지": f"OK: {조건.환경} 환경에서 {내식성} 내식성"
        })
    else:
        검증_결과.append({
            "항목": "환경 적합성",
            "통과": False,
            "메시지": f"경고: {조건.환경} 환경에 {내식성} 내식성 부적합"
        })

    # 결과 출력
    print("[검증 리포트]")
    print("=" * 40)
    전체_통과 = True
    for 검증 in 검증_결과:
        상태 = "[통과]" if 검증["통과"] else "[실패]"
        print(f"  {상태} {검증['항목']}: {검증['메시지']}")
        if not 검증["통과"]:
            전체_통과 = False

    print("=" * 40)
    if 전체_통과:
        print("  [결과] 모든 검증을 통과했습니다.")
    else:
        print("  [결과] 일부 검증에 실패했습니다. 설계를 검토해 주세요.")

    return {"통과": 전체_통과, "검증목록": 검증_결과}


# 검증 실행
print("[실행] 추천 결과 검증 중...")
