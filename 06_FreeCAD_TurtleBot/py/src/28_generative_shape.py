class 형태생성엔진:
    """AI 기반 형태 생성 반복 개선 엔진"""

    def __init__(self, 경계: 경계조건, 최대반복: int = 20):
        self.경계 = 경계
        self.최대반복 = 최대반복
        self.제안자 = AI형태제안자(경계)
        self.검증기 = 형태검증기(경계)
        self.이력: List[Dict] = []

    def 생성_및_개선(self) -> Tuple[형태파라메트릭, List[Dict]]:
        """형태를 생성하고 반복적으로 개선합니다."""
        print("\n" + "=" * 60)
        print("  AI 기반 형태 생성 시작")
        print("=" * 60)

        # 초기 제안
        현재형태 = self.제안자.초기_제안()
        최적형태 = 현재형태
        최적부피 = 현재형태.추정_부피()
        최적부피점수 = abs(최적부피 - 50000)  # 목표 부피 50000mm³

        for 반복 in range(1, self.최대반복 + 1):
            print(f"\n  [반복 {반복}/{self.최대반복}]")

            # 검증
            통과, 검증결과 = self.검증기.FreeCAD_모델_검증(현재형태)

            현재부피 = 검증결과.get("부피", 현재형태.추정_부피())
            문제점목록 = 검증결과.get("문제점", [])

            # 점수 계산 (목표 부피와의 차이)
            부피점수 = abs(현재부피 - 50000)
            print(f"    부피: {현재부피:.0f}mm³, 점수: {부피점수:.0f}, 문제: {len(문제점목록)}개")

            # 최적 갱신
            if 부피점수 < 최적부피점수:
                최적부피점수 = 부피점수
                최적형태 = 현재형태
                print(f"    -> 최적 갱신!")

            # 이력 기록
            self.이력.append({
                "반복": 반복,
                "부피": 현재부피,
                "점수": 부피점수,
                "통과": 통과,
                "문제점": 문제점목록,
            })

            # 수렴 확인
            if 부피점수 < 100:
                print("  [수렴] 목표 부피에 도달했습니다.")
                break

            # 피드백 생성
            피드백 = {}
            if "부피 초과" in 문제점목록:
                피드백["부피초과"] = True
            if "부피 부족" in 문제점목록:
                피드백["부피부족"] = True
            if "높이 초과" in 문제점목록:
                피드백["높이초과"] = True
            if "벽두께 부족" in 문제점목록:
                피드백["벽두께부족"] = True

            # 개선 제안 또는 무작위 변이
            if 반복 % 3 == 0:
                # 3회마다 무작위 변이로 탐색 다양화
                현재형태 = self.제안자.무작위_변이(최적형태, 변이율=0.15)
                print("    -> 무작위 변이 적용")
            else:
                현재형태 = self.제안자.개선_제안(현재형태, 피드백)

        print("\n" + "=" * 60)
        print("  형태 생성 완료")
        print(f"  최적 형태: {최적형태.형태유형}")
        print(f"  크기: {최적형태.가로:.1f} x {최적형태.세로:.1f} x {최적형태.높이:.1f}")
        print(f"  추정 부피: {최적형태.추정_부피():.0f} mm³")
        print("=" * 60)

        return 최적형태, self.이력


