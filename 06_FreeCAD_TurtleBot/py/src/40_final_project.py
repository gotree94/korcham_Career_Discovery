def 하우징_생성(최적결과, 보드정보, 센서정보, 요구사항):
    """
    최적화된 치수로 IoT 센서 노드 하우징을 생성합니다.

    매개변수:
        최적결과 (dict): 최적화된 치수
        보드정보 (dict): 보드 사양
        센서정보 (list): 센서 사양 목록
        요구사항 (프로젝트요구사항): 프로젝트 요구사항

    반환값:
        Part.Shape 또는 None
    """
    if not FREECAD_AVAILABLE:
        print("[시뮬레이션] FreeCAD에서 모델을 생성할 수 없습니다.")
        return None

    doc = FreeCAD.newDocument(f"{요구사항.프로젝트명}_v{요구사항.버전}")

    가로 = 최적결과["외부가로"]
    세로 = 최적결과["외부세로"]
    높이 = 최적결과["외부높이"]
    벽 = 최적결과["벽두께"]

    print(f"\n  [모델 생성] {가로} x {세로} x {높이} mm")

    # 1단계: 외부 본체
    print("    [1/8] 외부 본체...")
    외부 = Part.makeBox(가로, 세로, 높이)

    # 2단계: 내부 캐비티
    print("    [2/8] 내부 캐비티...")
    내부가로 = 가로 - 벽 * 2
    내부세로 = 세로 - 벽 * 2
    내부높이 = 높이 - 벽
    캐비티 = Part.makeBox(
        내부가로, 내부세로, 내부높이,
        Base.Vector(벽, 벽, 벽)
    )
    하우징 = 외부.cut(캐비티)

    # 3단계: 보드 마운트 홀
    print("    [3/8] 보드 마운트 홀...")
    for 위치 in 보드정보.get("마운트홀", []):
        홀 = Part.makeCylinder(
            보드정보["홀지름"] / 2,
            벽 + 1,
            Base.Vector(위치[0] + 벽, 위치[1] + 벽, 0),
            Base.Vector(0, 0, 1)
        )
        하우징 = 하우징.cut(홀)
    print(f"      보드 홀 {len(보드정보.get('마운트홀', []))}개 완료")

    # 4단계: 케이블 홀
    print("    [4/8] 케이블 통과 홀...")
    케이블지름 = 6.0
    if 요구사항.케이블출구 == "후면":
        케이블홀 = Part.makeCylinder(
            케이블지름 / 2, 벽 + 1,
            Base.Vector(가로 / 2, -0.5, 벽 + 3),
            Base.Vector(0, -1, 0)
        )
    else:  # 측면
        케이블홀 = Part.makeCylinder(
            케이블지름 / 2, 벽 + 1,
            Base.Vector(-0.5, 세로 / 2, 벽 + 3),
            Base.Vector(-1, 0, 0)
        )
    하우징 = 하우징.cut(케이블홀)

    # 5단계: 센서 홀/윈도우
    print("    [5/8] 센서 배치...")
    센서x_offset = 벽 + 5
    센서y_offset = 벽 + 보드정보.get("가로", 50) + 5

    for i, (센서이름, 정보) in enumerate(센서정보.items()):
        센서지름 = max(정보.get("가로", 10), 정보.get("세로", 10))
        센서z = 벽 + 5 + i * 12

        # 센서 홀 (측면)
        센서홀 = Part.makeCylinder(
            센서지름 / 2, 벽 + 1,
            Base.Vector(가로 / 2, -0.5, 센서z),
            Base.Vector(0, -1, 0)
        )
        하우징 = 하우징.cut(센서홀)
        print(f"      {센서이름} ({정보['유형']}) 홀 추가")

    # 6단계: 내부 리브
    print("    [6/8] 내부 리브...")
    리브두께 = 벽 * 0.8
    리브높이 = 내부높이 * 0.5

    # X방향 리브
    리브1 = Part.makeBox(
        리브두께, 내부세로, 리브높이,
        Base.Vector(가로 / 2 - 리브두께 / 2, 벽, 벽)
    )
    하우징 = 하우징.fuse(리브1)
    # Y방향 리브
    리브2 = Part.makeBox(
        내부가로, 리브두께, 리브높이,
        Base.Vector(벽, 세로 / 2 - 리브두께 / 2, 벽)
    )
    하우징 = 하우징.fuse(리브2)

    # 7단계: 벽걸이 홀
    print("    [7/8] 마운트 홀...")
    if 요구사항.마운트방식 == "벽걸이":
        # 상단 모서리에 벽걸이 홀 2개
        벽걸이z = 높이 - 5
        for x in [가로 * 0.2, 가로 * 0.8]:
            벽걸이홀 = Part.makeCylinder(
                2.0, 벽 + 2,
                Base.Vector(x, 세로 / 2, 벽걸이z),
                Base.Vector(0, 0, 1)
            )
            하우징 = 하우징.cut(벽걸이홀)
        print("      벽걸이 홀 2개 추가")

    # 8단계: 모서리 반경
    print("    [8/8] 모서리 마감...")
    반경 = 1.0
    try:
        모서리목록 = []
        for edge in 하우징.Edges:
            if edge.Length > min(가로, 세로) * 0.3:
                모서리목록.append(edge)
        if 모서리목록:
            하우징 = 하우징.makeFillet(반경, 모서리목록[:8])
    except Exception:
        pass

    # FreeCAD 문서에 추가
    obj = doc.addObject("Part::Feature", f"{요구사항.프로젝트명}")
    obj.Shape = 하우징
    doc.recompute()

    print(f"  [모델 생성] 완료!")
    return 하우징


