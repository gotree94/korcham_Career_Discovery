def 등가_지름_계산(shape):
    """
    셰이프의 단면을 기반으로 등가 지름을 계산합니다.
    강도 추정에 사용됩니다.

    매개변수:
        shape: 분석할 Part.Shape 객체

    반환값:
        float: 등가 지름 (단위: mm)
    """
    면적 = shape.Area
    if 면적 > 0:
        # 원형 단면의 등가 지름: D = 2 * sqrt(A / pi)
        등가_지름 = 2.0 * (면적 / Math.pi) ** 0.5
        return 등가_지름
    return 0.0


def 재료_제거_알고리즘(shape, 최소_두께=1.0, 공백_비율=0.3):
    """
    셰이프에서 불필요한 내부 재료를 제거합니다.
    지정된 최소 두께 이하의 부위를 hollow 처리합니다.

    매개변수:
        shape: 원본 Part.Shape 객체
        최소_두께: 유지할 최소 벽 두께 (mm)
        공백_비율: 제거할 공백 영역의 비율

    반환값:
        Part.Shape: 재료가 제거된 셰이프
    """
    bb = shape.BoundBox

    # 내부 박스 생성 (재료 제거 영역)
    내부_시작_X = bb.XMin + 최소_두께 + (bb.XLength - 2 * 최소_두께) * 공백_비율 * 0.5
    내부_시작_Y = bb.YMin + 최소_두께 + (bb.YLength - 2 * 최소_두께) * 공백_비율 * 0.5
    내부_시작_Z = bb.ZMin + 최소_두께 + (bb.ZLength - 2 * 최소_두께) * 공백_비율 * 0.5

    내부_길이_X = (bb.XLength - 2 * 최소_두께) * (1.0 - 공백_비율)
    내부_길이_Y = (bb.YLength - 2 * 최소_두께) * (1.0 - 공백_비율)
    내부_길이_Z = (bb.ZLength - 2 * 최소_두께) * (1.0 - 공백_비율)

    # 최소 크기 검증
    if 내부_길이_X <= 0 or 내부_길이_Y <= 0 or 내부_길이_Z <= 0:
        print("  경고: 최소 두께가 바운딩 박스보다 큽니다. 제거 불가.")
        return shape

    내부_박스 = Part.makeBox(
        내부_길이_X, 내부_길이_Y, 내부_길이_Z,
        FreeCAD.Vector(내부_시작_X, 내부_시작_Y, 내부_시작_Z),
    )

    # 원래 셰이프에서 내부 박스 차단 (불리안 뺄셈)
    try:
        결과 = shape.cut(내부_박스)
        print(f"  재료 제거 완료 - 원래 부피: {shape.Volume:.2f}mm³ → {결과.Volume:.2f}mm³")
        print(f"  제거된 재료 비율: {(1 - 결과.Volume / shape.Volume) * 100:.1f}%")
        return 결과
    except Exception as e:
        print(f"  재료 제거 실패: {e}")
        return shape


def 반복적_재료_제거(shape, 제거_횟수=3, 단계별_공백=0.1):
    """
    반복적으로 재료를 제거하여 점진적으로 경량화합니다.

    매개변수:
        shape: 원본 Part.Shape 객체
        제거_횟수: 재료 제거 반복 횟수
        단계별_공백: 각 단계에서 추가할 공백 비율

    반환값:
        Part.Shape: 최종 경량화된 셰이프
    """
    현재_shape = shape
    print(f"  반복적 재료 제거 시작 - 반복 횟수: {제거_횟수}")

    for i in range(제거_횟수):
        공백_비율 = 단계별_공백 * (i + 1)
        이전_부피 = 현재_shape.Volume

        현재_shape = 재료_제거_알고리즘(
            현재_shape, 최소_두께=0.5, 공백_비율=공백_비율
        )

        print(f"    단계 {i + 1}: 공백 {공백_비율 * 100:.0f}% → 부피: {현재_shape.Volume:.2f}mm³")

        # 부피 변화가 미미하면 조기 종료
        if 이전_부피 > 0 and abs(이전_부피 - 현재_shape.Volume) / 이전_부피 < 0.01:
            print(f"    단계 {i + 1}에서 수렴 - 조기 종료")
            break

    return 현재_shape


