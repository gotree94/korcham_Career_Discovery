def 바운딩_박스_분석(shape):
    """
    셰이프의 바운딩 박스를 분석하여 부피 효율성을 계산합니다.

    매개변수:
        shape: 분석할 Part.Shape 객체

    반환값:
        dict: 바운딩 박스 정보와 효율성 지표
    """
    bb = shape.BoundBox
    바운딩_박스_부피 = bb.XLength * bb.YLength * bb.ZLength

    # 실제 셰이프 부피 계산 (간접 계산법 사용)
    실제_부피 = shape.Volume

    if 바운딩_박스_부피 > 0:
        부피_효율성 = 실제_부피 / 바운딩_박스_부피
    else:
        부피_효율성 = 0.0

    결과 = {
        " minX": bb.XMin,
        " minY": bb.YMin,
        " minZ": bb.ZMin,
        " maxX": bb.XMax,
        " maxY": bb.YMax,
        " maxZ": bb.ZMax,
        " XLength": bb.XLength,
        " YLength": bb.YLength,
        " ZLength": bb.ZLength,
        " 바운딩_박스_부피": 바운딩_박스_부피,
        " 실제_부피": 실제_부피,
        " 부피_효율성": 부피_효율성,
    }
    return 결과


def 바운딩_박스_최적화(shape, 여유_비율=0.1):
    """
    셰이프를 바운딩 박스에 맞게 최적화합니다.
    불필요한 돌출부를 제거하고 더 컴팩트한 형태로 만듭니다.

    매개변수:
        shape: 최적화할 Part.Shape 객체
        여유_비율: 바운딩 박스 대비 여유 공간 비율 (0.0 ~ 0.5)

    반환값:
        Part.Shape: 최적화된 셰이프
    """
    bb = shape.BoundBox

    # 여유 공간 적용한 축소 바운딩 박스 계산
    여유_X = bb.XLength * 여유_비율
    여유_Y = bb.YLength * 여유_비율
    여유_Z = bb.ZLength * 여유_비율

    최적화된_박스 = Part.makeBox(
        bb.XLength - 2 * 여유_X,
        bb.YLength - 2 * 여유_Y,
        bb.ZLength - 2 * 여유_Z,
        FreeCAD.Vector(bb.XMin + 여유_X, bb.YMin + 여유_Y, bb.ZMin + 여유_Z),
    )

    # 원래 셰이프와의 교차로 최적화된 형태 생성
    try:
        결과 = shape.common(최적화된_박스)
        print(f"  바운딩 박스 최적화 완료 - 축소된 바운딩 박스로 교차 연산 수행")
        return 결과
    except Exception as e:
        print(f"  최적화 실패: {e}")
        return shape


