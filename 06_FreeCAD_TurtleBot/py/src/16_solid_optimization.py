# 3. 구조적 효율성 계산
# ============================================================

def 구조적_효율성_계산(shape):
    """
    셰이프의 구조적 효율성을 종합적으로 계산합니다.

    평가 항목:
      - 부피 효율성: 실제 부피 / 바운딩 박스 부피
      - 표면적 효율성: 표면적 / 부피
      - 단면 균일성: 단면 변화율
      - 종합 효율성 점수

    매개변수:
        shape: 분석할 Part.Shape 객체

    반환값:
        dict: 구조적 효율성 지표
    """
    bb = shape.BoundBox
    바운딩_박스_부피 = bb.XLength * bb.YLength * bb.ZLength

    # 1. 부피 효율성
    부피_효율성 = shape.Volume / 바운딩_박스_부피 if 바운딩_박스_부피 > 0 else 0.0

    # 2. 표면적 대 부피 비율 (비표면적 - 높을수록 가벼운 구조)
    if shape.Volume > 0:
        비표면적 = shape.Area / shape.Volume
    else:
        비표면적 = 0.0

    # 3. 형태 계수 (공학적 형태 복잡도 추정)
    #    완전 구형: 1.0, 긴 봉: 높은 값
    최대_치 = max(bb.XLength, bb.YLength, bb.ZLength)
    최소_치 = min(bb.XLength, bb.YLength, bb.ZLength)

    if 최대_치 > 0:
        형태_비율 = 최대_치 / 최소_치
    else:
        형태_비율 = 1.0

    # 형태 계수: 1에 가까울수록 균형 잡힌 형태
    형태_계수 = 1.0 / 형태_비율 if 형태_비율 > 0 else 0.0

    # 4. 종합 효율성 점수 (0 ~ 100)
    종합_점수 = (
        부피_효율성 * 40       # 부피 효율성 40%
        + 형태_계수 * 30       # 형태 균형 30%
        + min(비표면적 / 10, 1.0) * 30  # 비표면적 점수 30%
    )
    종합_점수 = min(종합_점수, 100.0)

    결과 = {
        " 부피 효율성": 부피_효율성,
        " 비표면적": 비표면적,
        " 형태_비율": 형태_비율,
        " 형태_계수": 형태_계수,
        " 종합_점수": 종합_점수,
    }

    return 결과


def 최적화_리포트_생성(원본_shape, 최적화된_shape):
    """
    최적화 전후의 비교 리포트를 생성합니다.

    매개변수:
        원본_shape: 최적화 전 Part.Shape
        최적화된_shape: 최적화 후 Part.Shape

    반환값:
        str: 비교 리포트 텍스트
    """
    원본_효율성 = 구조적_효율성_계산(원본_shape)
    최적화_효율성 = 구조적_효율성_계산(최적화된_shape)

    리포트 = []
    리포트.append("=" * 50)
    리포트.append("  졸리드 바디 최적화 리포트")
    리포트.append("=" * 50)
    리포트.append(f"  원본 부피:       {원본_shape.Volume:>12.2f} mm³")
    리포트.append(f"  최적화 후 부피:  {최적화된_shape.Volume:>12.2f} mm³")
    리포트.append(f"  부피 감소율:     {(1 - 최적화된_shape.Volume / 원본_shape.Volume) * 100:>11.1f} %")
    리포트.append(f"  원본 표면적:     {원본_shape.Area:>12.2f} mm²")
    리포트.append(f"  최적화 후 표면적:{최적화된_shape.Area:>12.2f} mm²")
    리포트.append("-" * 50)
    리포트.append(f"  원본 종합 점수:   {원본_효율성[' 종합_점수']:>10.1f}")
    리포트.append(f"  최적화 종합 점수: {최적화_효율성[' 종합_점수']:>10.1f}")
    리포트.append(f"  효율성 향상:      {최적화_효율성[' 종합_점수'] - 원본_효율성[' 종합_점수']:>+10.1f}")
    리포트.append("=" * 50)

    return "\n".join(리포트)


