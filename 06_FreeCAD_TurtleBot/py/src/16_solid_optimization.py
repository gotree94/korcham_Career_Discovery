def 예제_박스_생성(가로=40, 세로=30, 높이=50):
    """
    시연용 박스 모델을 생성합니다.

    매개변수:
        가로: X축 방향 크기 (mm)
        세로: Y축 방향 크기 (mm)
        높이: Z축 방향 크기 (mm)

    반환값:
        Part.Shape: 박스 셰이프
    """
    return Part.makeBox(가로, 세로, 높이)


def 예제_기둥_생성(지름=20, 높이=60):
    """
    시연용 원형 기둥 모델을 생성합니다.

    매개변수:
        지름: 기둥 직경 (mm)
        높이: 기둥 높이 (mm)

    반환값:
        Part.Shape: 원형 기둥 셰이프
    """
    return Part.makeCylinder(지름 / 2.0, 높이)


def 예제_강철_브라켓_생성():
    """
    시연용 강철 브라켓을 생성합니다.
    L자 형태의 브라켓에 구멍이 있는 구조입니다.

    반환값:
        Part.Shape: 브라켓 셰이프
    """
    # 수평 판
    수평_판 = Part.makeBox(60, 40, 5)
    # 수직 판
    수직_판 = Part.makeBox(5, 40, 40, FreeCAD.Vector(0, 0, 5))

    # 브라켓 결합
    브라켓 = 수평_판.fuse(수직_판)

    # 수평 판에 구멍 추가
    수평_구멍 = Part.makeCylinder(5, 5, FreeCAD.Vector(30, 20, -1), FreeCAD.Vector(0, 0, 1))
    브라켓 = 브라켓.cut(수평_구멍)

    # 수직 판에 구멍 추가
    수직_구멍 = Part.makeCylinder(4, 5, FreeCAD.Vector(2.5, 20, 25), FreeCAD.Vector(0, 1, 0))
    브라켓 = 브라켓.cut(수직_구멍)

    return 브라켓


