def 메인_실행():
    """
    졸리드 바디 최적화 메인 실행 함수입니다.
    모든 최적화 알고리즘을 순차적으로 실행하고 결과를 표시합니다.
    """
    print("=" * 60)
    print("  Part 4 - 졸리드 바디 최적화")
    print("  알고리즘 기반 설계 시리즈")
    print("=" * 60)

    # ----------------------------------------------------------
    # 시나리오 1: 바운딩 박스 기반 최적화
    # ----------------------------------------------------------
    print("\n[시나리오 1] 바운딩 박스 기반 최적화")
    print("-" * 50)

    원본_박스 = 예제_박스_생성(가로=40, 세로=30, 높이=50)
    분석_결과 = 바운딩_박스_분석(원본_박스)

    print(f"  원본 박스 크기: {분석_결과[' XLength']:.0f} x {분석_결과[' YLength']:.0f} x {분석_결과[' ZLength']:.0f} mm")
    print(f"  원본 부피: {분석_결과[' 실제_부피']:.2f} mm³")
    print(f"  부피 효율성: {분석_결과[' 부피_효율성'] * 100:.1f}%")

    # 최적화 수행
    최적화된_박스 = 바운딩_박스_최적화(원본_박스, 여유_비율=0.15)
    최적화_분석 = 바운딩_박스_분석(최적화된_박스)
    print(f"  최적화 후 부피: {최적화_분석[' 실제_부피']:.2f} mm³")
    print(f"  최적화 후 효율성: {최적화_분석[' 부피_효율성'] * 100:.1f}%")

    # ----------------------------------------------------------
    # 시나리오 2: 불필요한 재료 제거
    # ----------------------------------------------------------
    print("\n[시나리오 2] 불필요한 재료 제거 알고리즘")
    print("-" * 50)

    원본_기둥 = 예제_기둥_생성(지름=20, 높이=60)
    print(f"  원본 기둥 부피: {원본_기둥.Volume:.2f} mm³")

    # 단일 재료 제거
    print("\n  >> 단일 재료 제거:")
    경량화_기둥 = 재료_제거_알고리즘(원본_기둥, 최소_두께=2.0, 공백_비율=0.4)

    # 반복적 재료 제거
    print("\n  >> 반복적 재료 제거:")
    반복_경량화_기둥 = 반복적_재료_제거(원본_기둥, 제거_횟수=3, 단계별_공백=0.15)

    # ----------------------------------------------------------
    # 시나리오 3: 브라켓 최적화
    # ----------------------------------------------------------
    print("\n[시나리오 3] 브라켓 최적화")
    print("-" * 50)

    원본_브라켓 = 예제_강철_브라켓_생성()
    print(f"  원본 브라켓 부피: {원본_브라켓.Volume:.2f} mm³")
    print(f"  원본 브라켓 표면적: {원본_브라켓.Area:.2f} mm²")

    최적화된_브라켓 = 재료_제거_알고리즘(원본_브라켓, 최소_두께=1.5, 공백_비율=0.2)

    # ----------------------------------------------------------
    # 시나리오 4: 구조적 효율성 종합 평가
    # ----------------------------------------------------------
    print("\n[시나리오 4] 구조적 효율성 종합 평가")
    print("-" * 50)

    테스트_모델_목록 = [
        ("원본 박스", 원본_박스),
        ("최적화 박스", 최적화된_박스),
        ("원본 기둥", 원본_기둥),
        ("경량화 기둥", 반복_경량화_기둥),
        ("원본 브라켓", 원본_브라켓),
        ("최적화 브라켓", 최적화된_브라켓),
    ]

    for 이름, 모델 in 테스트_모델_목록:
        효율성 = 구조적_효율성_계산(모델)
        print(f"\n  [{이름}]")
        print(f"    부피: {모델.Volume:>10.2f} mm³")
        print(f"    표면적: {모델.Area:>10.2f} mm²")
        print(f"    부피 효율성: {효율성[' 부피 효율성'] * 100:>8.1f}%")
        print(f"    형태 계수: {효율성[' 형태_계수']:>12.3f}")
        print(f"    종합 점수: {효율성[' 종합_점수']:>10.1f}")

    # ----------------------------------------------------------
    # 최적화 리포트
    # ----------------------------------------------------------
    print("\n")
    리포트 = 최적화_리포트_생성(원본_브라켓, 최적화된_브라켓)
    print(리포트)

    # ----------------------------------------------------------
    # FreeCAD 문서에 결과 표시 (FreeCAD 실행 시)
    # ----------------------------------------------------------
    try:
        doc = FreeCAD.ActiveDocument
        if doc is None:
            doc = FreeCAD.newDocument("졸리드바디_최적화")

        # 원본 브라켓
        obj_원본 = doc.addObject("Part::Feature", "원본_브라켓")
        obj_원본.Shape = 원본_브라켓

        # 최적화된 브라켓 (위치 이동)
        obj_최적화 = doc.addObject("Part::Feature", "최적화된_브라켓")
        최적화된_브라켓_이동 = 최적화된_브라켓.copy()
        최적화된_브라켓_이동.translate(FreeCAD.Vector(80, 0, 0))
        obj_최적화.Shape = 최적화된_브라켓_이동

        # 경량화 기둥
        obj_기둥 = doc.addObject("Part::Feature", "경량화_기둥")
        최적화된_기둥_이동 = 반복_경량화_기둥.copy()
        최적화된_기둥_이동.translate(FreeCAD.Vector(160, 0, 0))
        obj_기둥.Shape = 최적화된_기둥_이동

        doc.recompute()
        print("\n  FreeCAD 문서에 결과 모델이 추가되었습니다.")
        print("  브라우저에서 '원본_브라켓', '최적화된_브라켓', '경량화_기둥'을 확인하세요.")
    except Exception as e:
        print(f"\n  FreeCAD 문서 작업 실패 (매크로 환경이 아닐 수 있음): {e}")

    print("\n" + "=" * 60)
    print("  졸리드 바디 최적화 완료!")
    print("=" * 60)


