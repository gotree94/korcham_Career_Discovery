def 프레임바디_생성(드론이름="중형드론_450", 파라미터=None):
    """
    드론 프레임 메인 바디를 생성한다.
    X 형태의 프레임 구조.

    매개변수:
        드론이름 (str): 드론 프리셋 키
        파라미터 (드론파라미터): 드론 파라미터

    반환값:
        Part.Shape: 프레임 바디 형태
    """
    if 파라미터 is None:
        파라미터 = 드론파라미터()

    if 드론이름 not in 드론프리셋:
        print(f"[오류] 알 수 없는 드론 프리셋: {드론이름}")
        return None

    프리셋 = 드론프리셋[드론이름]
    print(f"[정보] {프리셋['설명']} 프레임 바디 생성 중...")

    팔길이 = 프리셋["프레임팔길이"]
    팔너비 = 프리셋["프레임팔너비"]
    두께 = 프리셋["프레임두께"]

    # 중앙 허브
    허브지름 = 팔너비 * 3
    허브 = Part.makeCylinder(허브지름 / 2, 두께)

    # 4개의 팔 (X 형태)
    for 각도 in [45, 135, 225, 315]:
        라디안 = math.radians(각도)

        # 팔 생성 (회전된 상자)
        팔 = Part.makeBox(팔길이, 팔너비, 두께)
        팔변환 = Base.Matrix()
        팔변환.rotateZ(라디안)
        팔변환.move(Base.Vector(-팔너비 / 2, -팔너비 / 2, 0))
        팔 = 팔.transformGeometry(팔변환)

        허브 = 허브.fuse(팔)

    # 중앙 배터리 홀더 마운트 홀
    마운트홀지름 = 4.0 + 파라미터.공차
    for 각도 in [0, 90, 180, 270]:
        라디안 = math.radians(각도)
        x = math.cos(라디안) * 15.0
        y = math.sin(라디안) * 15.0

        홀 = Part.makeCylinder(
            마운트홀지름 / 2, 두께 + 1,
            Base.Vector(x, y, -0.5),
            Base.Vector(0, 0, 1)
        )
        허브 = 허브.cut(홀)

    print(f"[정보] 프레임 바디 생성 완료")
    return 허브


