@dataclass
class 검증항목:
    """단일 검증 항목 결과"""
    이름: str
    통과: bool
    메시지: str
    심각도: str = "정보"  # 정보, 경고, 오류, 치명적


@dataclass
class 검증보고서:
    """전체 검증 보고서"""
    모델명: str
    검증일시: str = ""
    항목들: List[검증항목] = field(default_factory=list)
    총검사수: int = 0
    통과수: int = 0
    실패수: int = 0

    def __post_init__(self):
        if not self.검증일시:
            self.검증일시 = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    def 항목_추가(self, 항목: 검증항목):
        self.항목들.append(항목)
        self.총검사수 += 1
        if 항목.통과:
            self.통과수 += 1
        else:
            self.실패수 += 1

    def 전체_통과(self) -> bool:
        """치명적/오류 레벨의 실패가 없으면 통과"""
        for 항목 in self.항목들:
            if not 항목.통과 and 항목.심각도 in ("오류", "치명적"):
                return False
        return True

    def 텍스트_리포트(self) -> str:
        """텍스트 기반 리포트를 생성합니다."""
        줄 = [
            "=" * 60,
            "  AI 모델 검증 보고서",
            "=" * 60,
            f"  모델명: {self.모델명}",
            f"  검증일시: {self.검증일시}",
            f"  총 검사: {self.총검사수}, 통과: {self.통과수}, 실패: {self.실패수}",
            f"  최종 판정: {'통과' if self.전체_통과() else '실패'}",
            "=" * 60,
            "",
        ]

        for 항목 in self.항목들:
            상태 = "OK" if 항목.통과 else "FAIL"
            줄.append(f"  [{상태}] [{항목.심각도}] {항목.이름}")
            줄.append(f"         {항목.메시지}")
            줄.append("")

        줄.append("=" * 60)
        return "\n".join(줄)


