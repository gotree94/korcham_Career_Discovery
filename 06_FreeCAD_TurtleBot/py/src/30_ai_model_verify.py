class 기하학적검사:
    """모델의 기하학적 유효성을 검사하는 클래스"""

    def __init__(self, 모델=None):
        self.모델 = 모델

    def 검사_실행(self, 모델=None) -> List[검증항목]:
        """기하학적 검사를 수행합니다."""
        검사결과 = []
        대상 = 모델 or self.모델

        if 대상 is None:
            검사결과.append(검증항목(
                이름="기하학적 존재",
                통과=False,
                메시지="검증할 모델이 없습니다.",
                심각도="치명적",
            ))
            return 검사결과

        if FREECAD_AVAILABLE and hasattr(대상, "Volume"):
            # FreeCAD 모델 검증
            검사결과.extend(self._FreeCAD_기하학_검증(대상))
        elif isinstance(대상, dict):
            # 파라메트릭 정보 기반 검증
            검사결과.extend(self._파라메트릭_기하학_검증(대상))
        else:
            # 기본 검증
            검사결과.append(self._공간_검증(대상))

        return 검사결과

    def _FreeCAD_기하학_검증(self, 모델) -> List[검증항목]:
        """FreeCAD 모델의 기하학적 검증"""
        결과 = []

        # 1. 부피 검사
        부피 = 모델.Volume
        if 부피 > 0:
            결과.append(검증항목(
                이름="양의 부피",
                통과=True,
                메시지=f"부피: {부피:.2f} mm³",
            ))
        else:
            결과.append(검증항목(
                이름="양의 부피",
               통과=False,
                메시지=f"부피가 0 이하입니다: {부피:.2f} mm³",
                심각도="치명적",
            ))

        # 2. 표면적 검사
        표면적 = 모델.Area
        if 표면적 > 0:
            결과.append(검증항목(
                이름="양의 표면적",
                통과=True,
                메시지=f"표면적: {표면적:.2f} mm²",
            ))
        else:
            결과.append(검증항목(
                이름="양의 표면적",
                통과=False,
                메시지=f"표면적이 0 이하입니다: {표면적:.2f} mm²",
                심각도="치명적",
            ))

        # 3. 연결성 검사
        try:
            지오메트리 = 모델.copy()
            if 지오메트리.isClosed():
                결과.append(검증항목(
                    이름="지오메트리 연결성",
                    통과=True,
                    메시지="모델이 닫힌 솔리드입니다.",
                ))
            else:
                결과.append(검증항목(
                    이름="지오메트리 연결성",
                   통과=False,
                    메시지="모델이 열린 형태입니다.",
                    심각도="경고",
                ))
        except Exception:
            결과.append(검증항목(
                이름="지오메트리 연결성",
                통과=True,
                메시지="연결성 검사를 건너뜁니다.",
            ))

        # 4. 경계선(Bounding Box) 검사
        BB = 모델.BoundBox
        if BB.isValid():
            크기 = (BB.XLength, BB.YLength, BB.ZLength)
            결과.append(검증항목(
                이름="경계선 유효성",
                통과=True,
                메시지=f"경계 크기: {크기[0]:.1f} x {크기[1]:.1f} x {크기[2]:.1f} mm",
            ))
        else:
            결과.append(검증항목(
                이름="경계선 유효성",
                통과=False,
                메시지="경계선(BoundBox)이 유효하지 않습니다.",
                심각도="오류",
            ))

        # 5. 자기교차 검사
        try:
            분할됨 = len(모델.ancestors()) > 1
            if 분할됨:
                결과.append(검증항목(
                    이름="단일체 검사",
                    통과=False,
                    메시지="모델이 여러 조각으로 분리되어 있습니다.",
                    심각도="경고",
                ))
            else:
                결과.append(검증항목(
                    이름="단일체 검사",
                    통과=True,
                    메시지="모델이 단일 연결체입니다.",
                ))
        except Exception:
            pass

        return 결과

    def _파라메트릭_기하학_검증(self, 파라메트릭: Dict) -> List[검증항목]:
        """파라메트릭 딕셔너리 기반 기하학 검증"""
        결과 = []

        # 크기 검사
        for 키 in ["가로", "세로", "높이"]:
            값 = 파라메트릭.get(키, 0)
            if 값 > 0:
                결과.append(검증항목(
                    이름=f"{키} 양수 검사",
                    통과=True,
                    메시지=f"{키}: {값:.2f} mm",
                ))
            else:
                결과.append(검증항목(
                    이름=f"{키} 양수 검사",
                    통과=False,
                    메시지=f"{키}가 0 이하입니다: {값}",
                    심각도="오류",
                ))

        # 부피 추정 검사
        x = 파라메트릭.get("가로", 0)
        y = 파라메트릭.get("세로", 0)
        z = 파라메트릭.get("높이", 0)
        부피 = x * y * z
        if 부피 > 0:
            결과.append(검증항목(
                이름="추정 부피",
                통과=True,
                메시지=f"추정 부피: {부피:.0f} mm³",
            ))

        return 결과

    def _공간_검증(self, 모델) -> 검증항목:
        """기본 공간 검증"""
        return 검증항목(
            이름="기본 검증",
            통과=True,
            메시지="기본 검증 통과 (FreeCAD 모드 아님)",
        )


