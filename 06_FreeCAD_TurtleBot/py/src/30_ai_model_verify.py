@dataclass
class 공차설정:
    """공차 검증 설정"""
    최소_두께: float = 1.0          # mm
    최대_길이비: float = 100.0       # 최대/최소 치수 비율
    최대_경사각: float = 45.0        # degree
    최소_모서리반경: float = 0.5     # mm
    공차_허용범위: float = 0.1       # mm (기본 공차)


class 공차검증기:
    """모델의 공차를 검증하는 클래스"""

    def __init__(self, 공차: 공차설정 = None):
        self.공차 = 공차 or 공차설정()

    def 검사_실행(self, 모델=None, 파라메트릭: Dict = None) -> List[검증항목]:
        """공차 검사를 수행합니다."""
        결과 = []

        if FREECAD_AVAILABLE and 모델 is not None:
            결과.extend(self._FreeCAD_공차검증(모델))
        elif 파라메트릭 is not None:
            결과.extend(self._파라메트릭_공차검증(파라메트릭))
        else:
            결과.append(검증항목(
                이름="공차 검증",
                통과=True,
                메시지="검증 대상이 없어 공차 검증을 건너뜁니다.",
            ))

        return 결과

    def _FreeCAD_공차검증(self, 모델) -> List[검증항목]:
        """FreeCAD 모델의 공차 검증"""
        결과 = []

        # 1. 최소 두께 검사
        try:
            BB = 모델.BoundBox
            최소치수 = min(BB.XLength, BB.YLength, BB.ZLength)
            if 최소치수 >= self.공차.최소_두께:
                결과.append(검증항목(
                    이름="최소 두께",
                    통과=True,
                    메시지=f"최소 치수: {최소치수:.2f}mm (기준: {self.공차.최소_두께}mm)",
                ))
            else:
                결과.append(검증항목(
                    이름="최소 두께",
                    통과=False,
                    메시지=f"최소 치수 {최소치수:.2f}mm < 기준 {self.공차.최소_두께}mm",
                    심각도="오류",
                ))
        except Exception:
            pass

        # 2. 치수 비율 검사
        try:
            BB = 모델.BoundBox
            치수들 = sorted([BB.XLength, BB.YLength, BB.ZLength])
            if 치수들[0] > 0:
                비율 = 치수들[2] / 치수들[0]
                if 비율 <= self.공차.최대_길이비:
                    결과.append(검증항목(
                        이름="치수 비율",
                        통과=True,
                        메시지=f"최대/최소 비율: {비율:.1f} (기준: {self.공차.최대_길이비} 이하)",
                    ))
                else:
                    결과.append(검증항목(
                        이름="치수 비율",
                        통과=False,
                        메시지=f"비율 {비율:.1f} > 기준 {self.공차.최대_길이비}",
                        심각도="경고",
                    ))
        except Exception:
            pass

        # 3. 단면적 변화 검사
        try:
            면적들 = []
            for ax in ["X", "Y", "Z"]:
                BB = 모델.BoundBox
                if ax == "X":
                    범위 = BB.XLength
                elif ax == "Y":
                    범위 = BB.YLength
                else:
                    범위 = BB.ZLength
                면적들.append(범위)

            면적비 = max(면적들) / max(min(면적들), 0.001)
            결과.append(검증항목(
                이름="단면 균일도",
                통과=True,
                메시지=f"단면 비율: {면적비:.2f}",
            ))
        except Exception:
            pass

        return 결과

    def _파라메트릭_공차검증(self, 파라메트릭: Dict) -> List[검증항목]:
        """파라메트릭 정보 기반 공차 검증"""
        결과 = []

        x = 파라메트릭.get("가로", 0)
        y = 파라메트릭.get("세로", 0)
        z = 파라메트릭.get("높이", 0)

        # 두께 검사
        벽두께 = 파라메트릭.get("벽두께", 5)
        if 벽두께 >= self.공차.최소_두께:
            결과.append(검증항목(
                이름="벽 두께",
                통과=True,
                메시지=f"벽 두께: {벽두께:.1f}mm (기준: {self.공차.최소_두께}mm)",
            ))
        else:
            결과.append(검증항목(
                이름="벽 두께",
                통과=False,
                메시지=f"벽 두께 {벽두께:.1f}mm < 기준 {self.공차.최소_두께}mm",
                심각도="오류",
            ))

        # 치수 비율 검사
        치수들 = sorted([x, y, z])
        if 치수들[0] > 0:
            비율 = 치수들[2] / 치수들[0]
            if 비율 <= self.공차.최대_길이비:
                결과.append(검증항목(
                    이름="치수 비율",
                    통과=True,
                    메시지=f"비율: {비율:.1f} (기준: {self.공차.최대_길이비} 이하)",
                ))
            else:
                결과.append(검증항목(
                    이름="치수 비율",
                    통과=False,
                    메시지=f"비율 {비율:.1f} > 기준 {self.공차.최대_길이비}",
                    심각도="경고",
                ))

        # 모서리 반경 검사
        모서리반경 = 파라메트릭.get("모서리반경", 0)
        if 모서리반경 >= self.공차.최소_모서리반경:
            결과.append(검증항목(
                이름="모서리 반경",
                통과=True,
                메시지=f"모서리 반경: {모서리반경:.1f}mm",
            ))
        elif 모서리반경 > 0:
            결과.append(검증항목(
                이름="모서리 반경",
                통과=False,
                메시지=f"모서리 반경 {모서리반경:.1f}mm < 기준 {self.공차.최소_모서리반경}mm",
                심각도="경고",
            ))

        return 결과


