    def _파라메트릭_제조검증(self, 파라메트릭: Dict) -> List[검증항목]:
        """파라메트릭 기반 제조 가능성 검증"""
        결과 = []

        x = 파라메트릭.get("가로", 0)
        y = 파라메트릭.get("세로", 0)
        z = 파라메트릭.get("높이", 0)
        벽두께 = 파라메트릭.get("벽두께", 5)

        # 1. 높이/바닥 비율 (금형 방향)
        바닥면적 = x * y
        if 바닥면적 > 0:
            높이비 = z / math.sqrt(바닥면적)
            if 높이비 <= self.검사규칙["최대_절삭비율"]:
                결과.append(검증항목(
                    이름="높이/바닥 비율",
                    통과=True,
                    메시지=f"높이 비율: {높이비:.2f} (기준: {self.검사규칙['최대_절삭비율']} 이하)",
                ))
            else:
                결과.append(검증항목(
                    이름="높이/바닥 비율",
                    통과=False,
                    메시지=f"높이 비율 {높이비:.2f} > 기준 {self.검사규칙['최대_절삭비율']}",
                    심각도="경고",
                ))

        # 2. 벽 두께 균일도
        if 벽두께 >= self.검사규칙["최소_절삭깊이"]:
            결과.append(검증항목(
                이름="절삭 가능성",
                통과=True,
                메시지=f"최소 벽 두께: {벽두께:.1f}mm",
            ))
        else:
            결과.append(검증항목(
                이름="절삭 가능성",
                통과=False,
                메시지=f"최소 벽 두께 {벽두께:.1f}mm < {self.검사규칙['최소_절삭깊이']}mm",
                심각도="오류",
            ))

        # 3. 모서리 반경 (밀링 가능)
        모서리반경 = 파라메트릭.get("모서리반경", 0)
        if 모서리반경 > 0:
            결과.append(검증항목(
                이름="모서리 가공",
                통과=True,
                메시지=f"모서리 반경: {모서리반경:.1f}mm",
            ))
        else:
            결과.append(검증항목(
                이름="모서리 가공",
                통과=False,
                메시지="모서리 반경이 설정되지 않았습니다.",
                심각도="정보",
            ))

        # 4. 가공 공정 추천
        공정추천 = []
        if 벽두께 >= 2.0 and x <= 500:
            공정추천.append("밀링")
        if 벽두께 < 2.0:
            공정추천.append("절삭가공")
        if z > 200:
            공정추천.append("대형밀링")

        결과.append(검증항목(
            이름="공정 추천",
            통과=True,
            메시지=f"추천 공정: {', '.join(공정추천) if 공정추천 else '표준 가공'}",
        ))

        return 결과


