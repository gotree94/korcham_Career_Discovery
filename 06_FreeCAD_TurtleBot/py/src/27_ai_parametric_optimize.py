class 무작위탐색:
    """단순 무작위 탐색 알고리즘"""

    def __init__(self, 변수목록: List[파라메트릭변수], 목적함수_객체: Callable):
        self.변수목록 = 변수목록
        self.목적함수 = 목적함수_객체

    def 탐색(self, 반복횟수: int = 100) -> Dict:
        """무작위 탐색을 수행합니다."""
        print(f"\n[무작위 탐색] 시작 (반복: {반복횟수}회)")

        최적값 = float("inf")
        최적변수 = {}
        이력: List[Dict] = []

        for i in range(반복횟수):
            # 무작위 변수 생성
            변수값 = {}
            for 변수 in self.변수목록:
                변수값[변수.이름] = random.uniform(변수.최소값, 변수.최대값)

            # 목적 함수 계산
            점수 = self.목적함수(변수값)
            이력.append({"반복": i + 1, "변수": 변수값.copy(), "점수": 점수})

            # 최적값 갱신
            if 점수 < 최적값:
                최적값 = 점수
                최적변수 = 변수값.copy()

            if (i + 1) % 20 == 0:
                print(f"  반복 {i+1}/{반복횟수} - 현재 최적: {최적값:.4f}")

        return {
            "알고리즘": "무작위 탐색",
            "최적값": 최적값,
            "최적변수": 최적변수,
            "이력": 이력,
            "반복횟수": 반복횟수,
        }


class AI추천탐색:
    """AI 추천 기반 탐색 알고리즘 (그리디 + 방향성 탐색)"""

    def __init__(self, 변수목록: List[파라메트릭변수], 목적함수_객체: Callable):
        self.변수목록 = 변수목록
        self.목적함수 = 목적함수_객체

    def _변수_추천(self, 현재값: Dict[str, float], 현재점수: float,
                    탐색배율: float = 0.1) -> Dict[str, float]:
        """현재 점수를 기반으로 더 나은 변수값을 추천합니다."""
        추천값 = 현재값.copy()

        for 변수 in self.변수목록:
            이름 = 변수.이름
            현재 = 현재값[이름]
            범위 = 변수.최대값 - 변수.최소값

            # 각 변수를 미세하게 변경하여 개선 방향 탐색
            변화량 = 범위 * 탐색배율 * random.choice([-1, 1])
            후보 = 변수.범위_내_값(현재 + 변화량)

            # 개선 여부 확인
            후보값 = 추천값.copy()
            후보값[이름] = 후보
            후보점수 = self.목적함수(후보값)

            if 후보점수 < 현재점수:
                추천값[이름] = 후보

        return 추천값

    def 탐색(self, 반복횟수: int = 100) -> Dict:
        """AI 추천 기반 탐색을 수행합니다."""
        print(f"\n[AI 추천 탐색] 시작 (반복: {반복횟수}회)")

        # 초기값 설정
        현재변수 = {}
        for 변수 in self.변수목록:
            현재변수[변수.이름] = 변수.초기값

        현재점수 = self.목적함수(현재변수)
        최적값 = 현재점수
        최적변수 = 현재변수.copy()
        이력: List[Dict] = []

        for i in range(반복횟수):
            # 탐색 배율을 점진적으로 감소 (탐색 정밀도 향상)
            탐색배율 = 0.3 * math.exp(-i / (반복횟수 * 0.3))

            # AI 추천 기반 변수 갱신
            추천변수 = self._변수_추천(현재변수, 현재점수, 탐색배율)
            추천점수 = self.목적함수(추천변수)

            if 추천점수 < 현재점수:
                현재변수 = 추천변수
                현재점수 = 추천점수
            else:
                # 개선 실패 시 무작위 재시작
                for 변수 in self.변수목록:
                    현재변수[변수.이름] = random.uniform(변수.최소값, 변수.최대값)
                현재점수 = self.목적함수(현재변수)

            # 최적값 갱신
            if 현재점수 < 최적값:
                최적값 = 현재점수
                최적변수 = 현재변수.copy()

            이력.append({"반복": i + 1, "변수": 현재변수.copy(), "점수": 현재점수})

            if (i + 1) % 20 == 0:
                print(f"  반복 {i+1}/{반복횟수} - 현재 최적: {최적값:.4f}")

        return {
            "알고리즘": "AI 추천 탐색",
            "최적값": 최적값,
            "최적변수": 최적변수,
            "이력": 이력,
            "반복횟수": 반복횟수,
        }


