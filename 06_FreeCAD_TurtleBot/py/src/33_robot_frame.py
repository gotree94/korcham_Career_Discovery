# 베이스 플레이트 생성
# ============================================================

def 베이스플레이트_생성(프리셋이름, 공차=None):
    """
    로봇 베이스 플레이트를 생성한다.

    매개변수:
        프리셋이름 (str): 로봇 프리셋 키
        공차 (공차설정): 공차 설정

    반환값:
        Part.Shape: 베이스 플레이트 형태
    """
    if 공차 is None:
        공차 = 공차설정()

    if 프리셋이름 not in 로봇프리셋:
        print(f"[오류] 알 수 없는 프리셋: {프리셋이름}")
        return None

    프리셋 = 로봇프리셋[프리셋이름]
    print(f"[정보] {프리셋['설명']} 베이스 플레이트 생성 중...")

    가로 = 프리셋["베이스가로"]
    세로 = 프리셋["베이스세로"]
    두께 = 프리셋["베이스두께"]

    # 베이스 플레이트 본체
    베이스 = Part.makeBox(가로, 세로, 두께)

    # 중앙 서보 마운트 홀
    서보정보 = 서보크기[프리셋["서보크기"]]
    중앙x = 가로 / 2
    중앙y = 세로 / 2

    # 서보 플랜지 홈
    플랜지x = 서보정보["플랜지가로"] + 공차.서보마운트공차
    플랜지y = 서보정보["플랜지세로"] + 공차.서보마운트공차
    플랜지z = 서보정보["플랜지높이"]

    플랜지홈 = Part.makeBox(
        플랜지x, 플랜지y, 플랜지z + 1,
        Base.Vector(중앙x - 플랜지x / 2, 중앙y - 플랜지y / 2, 두께 - 플랜지z - 0.5)
    )
    베이스 = 베이스.cut(플랜지홈)

    # 서보 마운트 나사 홀
    홀간격 = 서보정보["마운트홀간격"]
    홀지름 = 서보정보["마운트홀지름"] + 공차.나사공차

    나사위치 = [
        Base.Vector(중앙x - 홀간격 / 2, 중앙y - 홀간격 / 2, 0),
        Base.Vector(중앙x + 홀간격 / 2, 중앙y - 홀간격 / 2, 0),
        Base.Vector(중앙x - 홀간격 / 2, 중앙y + 홀간격 / 2, 0),
        Base.Vector(중앙x + 홀간격 / 2, 중앙y + 홀간격 / 2, 0),
    ]

    for 위치 in 나사위치:
        홀 = Part.makeCylinder(
            홀지름 / 2, 두께 + 1,
            Base.Vector(위치.x, 위치.y, -0.5),
            Base.Vector(0, 0, 1)
        )
        베이스 = 베이스.cut(홀)

    # 베이스 고정 홀 (테이블/프레임에 고정)
    고정홀간격x = 가로 * 0.8
    고정홀간격y = 세로 * 0.8
    고정홀지름 = 4.0 + 공차.나사공차

    고정위치 = [
        Base.Vector(가로 / 2 - 고정홀간격x / 2, 세로 / 2 - 고정홀간격y / 2, 0),
        Base.Vector(가로 / 2 + 고정홀간격x / 2, 세로 / 2 - 고정홀간격y / 2, 0),
        Base.Vector(가로 / 2 - 고정홀간격x / 2, 세로 / 2 + 고정홀간격y / 2, 0),
        Base.Vector(가로 / 2 + 고정홀간격x / 2, 세로 / 2 + 고정홀간격y / 2, 0),
    ]

    for 위치 in 고정위치:
        홀 = Part.makeCylinder(
            고정홀지름 / 2, 두께 + 1,
            Base.Vector(위치.x, 위치.y, -0.5),
            Base.Vector(0, 0, 1)
        )
        베이스 = 베이스.cut(홀)

    print(f"[정보] 베이스 플레이트 생성 완료: {가로}x{세로}x{두께}mm")
    return 베이스


