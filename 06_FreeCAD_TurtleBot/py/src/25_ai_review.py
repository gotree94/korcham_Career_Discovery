def 개선_설계_제안(모델정보, 규칙결과):
    """
    검수 결과를 바탕으로 개선된 설계를 제안합니다.

    매개변수:
        모델정보 (모델정보): 원본 모델 정보
        규칙결과 (list): 규칙 기반 검수 결과

    반환값:
        str: 개선된 설계의 FreeCAD 스크립트
    """
    스크립트 = '# -*- coding: utf-8 -*-\n'
    스크립트 += '# 검수 결과 기반 개선된 설계\n'
    스크립트 += f'# 생성일: {datetime.datetime.now().strftime("%Y-%m-%d %H:%M")}\n'
    스크립트 += f'# 원본 문서: {모델정보.문서명}\n\n'
    스크립트 += 'import FreeCAD\n'
    스크립트 += 'import Part\n\n'

    # 검수 결과에서 개선 사항 수집
    개선사항 = []
    for 결과 in 규칙결과:
        if 결과["등급"] in ["오류", "경고"] and 결과["개선"]:
            개선사항.append(결과)

    스크립트 += f'# 검수에서 발견된 문제: {len(개선사항)}건\n\n'
    스크립트 += 'doc = FreeCAD.newDocument("개선된_설계")\n\n'

    # 각 객체에 대해 개선 적용
    for obj in 모델정보.객체목록:
        이름 = obj["이름"]
        유형 = obj["유형"]
        속성 = obj.get("속성", {})
        bb = obj.get("바운딩박스", {})

        if 유형 == "Part::Box":
            길이 = 속성.get("Length", bb.get("가로", 100))
            너비 = 속성.get("Width", bb.get("세로", 50))
            높이 = 속성.get("Height", bb.get("높이", 25))

            # 개선: 최소 두께 보장
            if 높이 < 규칙.최소두께_mm:
                높이 = 규칙.최소두께_mm
                스크립트 += f'# [개선] {이름}: 두께를 {규칙.최소두께_mm}mm로 증가\n'

            스크립트 += f'{이름} = doc.addObject("Part::Box", "{이름}")\n'
            스크립트 += f'{이름}.Length = {길이}\n'
            스크립트 += f'{이름}.Width = {너비}\n'
            스크립트 += f'{이름}.Height = {높이}\n\n'

        elif 유형 == "Part::Cylinder":
            반지름 = 속성.get("Radius", bb.get("가로", 10) / 2)
            높이 = 속성.get("Height", bb.get("높이", 50))

            # 개선: 최소 지름 보장
            지름 = 반지름 * 2
            if 지름 < 규칙.최소지름_mm:
                반지름 = 규칙.최소지름_mm / 2
                스크립트 += f'# [개선] {이름}: 지름을 {규칙.최소지름_mm}mm로 증가\n'

            스크립트 += f'{이름} = doc.addObject("Part::Cylinder", "{이름}")\n'
            스크립트 += f'{이름}.Radius = {반지름}\n'
            스크립트 += f'{이름}.Height = {높이}\n\n'

    스크립트 += 'FreeCAD.ActiveDocument.recompute()\n'
    스크립트 += 'print("[성공] 개선된 설계 생성 완료")\n'

    print("[개선된 설계 스크립트]")
    print("-" * 40)
    print(스크립트)
    print("-" * 40)

    return 스크립트


# 개선 설계 제안
