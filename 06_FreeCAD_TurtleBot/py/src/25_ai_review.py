class 모델정보:
    """
    FreeCAD 모델의 정보를 저장하는 클래스입니다.
    FreeCAD 환경에서는 실제로 모델에서 정보를 추출하고,
    오프라인에서는 샘플 데이터를 사용합니다.
    """

    def __init__(self):
        self.문서명 = "미정"
        self.객체목록 = []
        self.전체부피 = 0.0
        self.전체표면적 = 0.0
        self.바운딩박스 = None

    def FreeCAD에서_추출(self):
        """
        FreeCAD 환경에서 현재 활성 문서의 모델 정보를 추출합니다.
        FreeCAD가 아닌 환경에서는 False를 반환합니다.
        """
        try:
            import FreeCAD
            import FreeCADGui

            doc = FreeCAD.ActiveDocument
            if doc is None:
                print("[정보] 활성 FreeCAD 문서가 없습니다.")
                return False

            self.문서명 = doc.Name
            print(f"[FreeCAD] 문서: {self.문서명}")

            전체부피 = 0.0
            전체표면적 = 0.0
            객체목록 = []

            for obj in doc.Objects:
                if hasattr(obj, "Shape"):
                    shape = obj.Shape
                    객체_정보 = {
                        "이름": obj.Name,
                        "유형": obj.TypeId,
                        "부피": shape.Volume if hasattr(shape, "Volume") else 0,
                        "표면적": shape.Area if hasattr(shape, "Area") else 0,
                    }

                    # 바운딩박스 정보
                    if hasattr(shape, "BoundBox"):
                        bb = shape.BoundBox
                        객체_정보["바운딩박스"] = {
                            "가로": bb.XLength,
                            "세로": bb.YLength,
                            "높이": bb.ZLength
                        }

                    # 속성 정보
                    속성 = {}
                    for prop in obj.PropertiesList:
                        if prop in ["Length", "Width", "Height", "Radius",
                                   "Radius1", "Radius2", "Angle"]:
                            try:
                                속성[prop] = getattr(obj, prop)
                            except Exception:
                                pass
                    객체_정보["속성"] = 속성

                    객체목록.append(객체_정보)
                    전체부피 += 객체_정보["부피"]
                    전체표면적 += 객체_정보["표면적"]

            self.객체목록 = 객체목록
            self.전체부피 = 전체부피
            self.전체표면적 = 전체표면적

            print(f"  - 객체 수: {len(객체목록)}")
            print(f"  - 전체 부피: {전체부피:.2f} mm3")
            print(f"  - 전체 표면적: {전체표면적:.2f} mm2")

            return True

        except ImportError:
            print("[정보] FreeCAD 환경이 아닙니다.")
            return False
        except Exception as e:
            print(f"[오류] 모델 추출 실패: {e}")
            return False

