def 검수_리포트_생성(모델정보, 규칙결과, AI결과=None):
    """
    검수 결과를 종합적인 리포트로 생성합니다.

    매개변수:
        모델정보 (모델정보): 모델 정보
        규칙결과 (list): 규칙 기반 검수 결과
        AI결과 (str): AI 검수 결과 (선택)

    반환값:
        str: 검수 리포트 전체 텍스트
    """
    now = datetime.datetime.now()

    리포트 = []
    리포트.append("=" * 60)
    리포트.append("FreeCAD 설계 검수 리포트")
    리포트.append("=" * 60)
    리포트.append(f"작성일: {now.strftime('%Y-%m-%d %H:%M:%S')}")
    리포트.append(f"문서명: {모델정보.문서명}")
    리포트.append("")

    # 모델 개요
    리포트.append("1. 모델 개요")
    리포트.append("-" * 40)
    리포트.append(f"  객체 수: {len(모델정보.객체목록)}")
    리포트.append(f"  전체 부피: {모델정보.전체부피:.2f} mm3")
    리포트.append(f"  전체 표면적: {모델정보.전체표면적:.2f} mm2")
    리포트.append("")

    for obj in 모델정보.객체목록:
        리포트.append(f"  [{obj['이름']}] {obj['유형']}")
        if "바운딩박스" in obj:
            bb = obj["바운딩박스"]
            리포트.append(f"    크기: {bb['가로']:.1f} x {bb['세로']:.1f} x {bb['높이']:.1f} mm")
        리포트.append(f"    부피: {obj['부피']:.2f} mm3")
    리포트.append("")

    # 규칙 기반 검수 결과
    리포트.append("2. 규칙 기반 검수 결과")
    리포트.append("-" * 40)
    리포트.append(f"  검수 항목 수: {len(규칙결과)}")
    리포트.append(f"  오류: {sum(1 for r in 규칙결과 if r['등급'] == '오류')}건")
    리포트.append(f"  경고: {sum(1 for r in 규칙결과 if r['등급'] == '경고')}건")
    리포트.append(f"  통과: {sum(1 for r in 규칙결과 if r['등급'] == '통과')}건")
    리포트.append("")

    for 결과 in 규칙결과:
        if 결과["등급"] != "통과":
            리포트.append(f"  [{결과['등급']}] {결과['항목']} ({결과['객체']})")
            리포트.append(f"    {결과['메시지']}")
            if 결과["개선"]:
                리포트.append(f"    개선: {결과['개선']}")
            리포트.append("")

    # AI 검수 결과
    if AI결과:
        리포트.append("3. AI 전문가 검수 결과")
        리포트.append("-" * 40)
        리포트.append(AI결과)
        리포트.append("")

    # 종합 판정
    리포트.append("4. 종합 판정")
    리포트.append("-" * 40)

    오류_수 = sum(1 for r in 규칙결과 if r["등급"] == "오류")
    경고_수 = sum(1 for r in 규칙결과 if r["등급"] == "경고")

    if 오류_수 > 0:
        판정 = "D (재설계 필요)"
        설명 = f"오류 {오류_수}건 발견. 치수 및 형상 전면 재검토 필요."
    elif 경고_수 > 3:
        판정 = "C (보정 필요)"
        설명 = f"경고 {경고_수}건. 주요 항목 보정 후 재검수 권장."
    elif 경고_수 > 0:
        판정 = "B (양호)"
        설명 = f"경고 {경고_수}건. 선택적 개선으로 설계 완성 가능."
    else:
        판정 = "A (우수)"
        설명 = "모든 검수 항목 통과. 설계 우수."

    리포트.append(f"  등급: {판정}")
    리포트.append(f"  코멘트: {설명}")
    리포트.append("")
    리포트.append("=" * 60)

    리포트_텍스트 = "\n".join(리포트)

    print(리포트_텍스트)

    return 리포트_텍스트


# 리포트 생성
print("[실행] 검수 리포트 생성 중...")
