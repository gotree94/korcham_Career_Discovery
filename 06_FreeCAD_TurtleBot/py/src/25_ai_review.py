def 규칙_검수(모델정보, 규칙):
    """
    정의된 설계 규칙에 따라 모델을 검수합니다.

    검수 항목:
    1. 형상 규칙 검증
    2. 치수 합리성 검증
    3. 구조적 건전성 검증
    4. 제작 가능성 검증
    5. 최적화 가능성 검토

    매개변수:
        모델정보 (모델정보): 모델의 정보
        규칙 (설계규칙): 설계 규칙

    반환값:
        list: 검수 결과 목록
    """
    검수결과 = []

    # 1. 형상 규칙 검증
    for obj in 모델정보.객체목록:
        이름 = obj["이름"]

        # 바운딩박스 기반 검사
        if "바운딩박스" in obj:
            bb = obj["바운딩박스"]
            가로 = bb.get("가로", 0)
            세로 = bb.get("세로", 0)
            높이 = bb.get("높이", 0)

            # 장단 비율 검사
            치수들 = [가로, 세로, 높이]
            if max(치수들) > 0 and min(치수들) > 0:
                비율 = max(치수들) / min(치수들)
                if 비율 > 규칙.최대비율:
                    검수결과.append({
                        "등급": "경고",
                        "항목": "장단 비율",
                        "객체": 이름,
                        "메시지": f"장단 비율 {비율:.1f}:1 (허용: {규칙.최대비율}:1)",
                        "개선": "리브 추가 또는 형상 변경 검토"
                    })
                else:
                    검수결과.append({
                        "등급": "통과",
                        "항목": "장단 비율",
                        "객체": 이름,
                        "메시지": f"비율 {비율:.1f}:1 - 적합",
                        "개선": ""
                    })

        # 속성 기반 검사
        속성 = obj.get("속성", {})

        # 원통 객체 검사
        if "Radius" in 속성:
            반지름 = 속성["Radius"]
            지름 = 반지름 * 2
            if 지름 < 규칙.최소지름_mm:
                검수결과.append({
                    "등급": "오류",
                    "항목": "최소 치수",
                    "객체": 이름,
                    "메시지": f"지름 {지름}mm < 최소 {규칙.최소지름_mm}mm",
                    "개선": f"지름을 {규칙.최소지름_mm}mm 이상으로 증가"
                })

            # 원통의 길이/지름 비율
            높이 = 속성.get("Height", 0)
            if 높이 > 0 and 지름 > 0:
                종비율 = 높이 / 지름
                if 종비율 > 20:
                    검수결과.append({
                        "등급": "경고",
                        "항목": "세로비 검사",
                        "객체": 이름,
                        "메시지": f"세로비 {종비율:.1f} - 가늘고 긴 형태",
                        "개선": "지지 구조 추가 또는 직경 증가 검토"
                    })

        # 상자 객체 검사
        if obj["유형"] == "Part::Box":
            길이 = 속성.get("Length", 0)
            너비 = 속성.get("Width", 0)
            높이 = 속성.get("Height", 0)

            # 최소 두께 검사 (높이가 벽 두께로 가정)
            if 높이 > 0 and 높이 < 규칙.최소두께_mm:
                검수결과.append({
                    "등급": "경고",
                    "항목": "최소 두께",
                    "객체": 이름,
                    "메시지": f"높이(두께) {높이}mm < 최소 {규칙.최소두께_mm}mm",
                    "개선": f"두께를 {규칙.최소두께_mm}mm 이상으로 증가"
                })

            # 체적 대비 표면적 비율 (공정 복잡도 지표)
            if 길이 > 0 and 너비 > 0 and 높이 > 0:
                체적 = 길이 * 너비 * 높이
                표면적 = 2 * (길이 * 너비 + 너비 * 높이 + 길이 * 높이)
                if 체적 > 0:
                    비율 = 표면적 / 체적
                    if 비율 > 0.5:
                        검수결과.append({
                            "등급": "정보",
                            "항목": "공정 복잡도",
                            "객체": 이름,
                            "메시지": f"표면적/체적 비율 {비율:.3f} - 얇은 벽 구조",
                            "개선": "제작 비용 확인 필요"
                        })

    # 전체 모델 검사
    # 객체 간 간격 검사 (간소화된 버전)
    if len(모델정보.객체목록) > 1:
        검수결과.append({
            "등급": "정보",
            "항목": "객체 간 간격",
            "객체": "전체",
            "메시지": f"객체 {len(모델정보.객체목록)}개 간 간격 검증 필요",
            "개선": "FreeCAD에서 간접 충돌 검사 실행 권장"
        })

    return 검수결과


